#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum CliCommandPolicy {
    Local,
    Observe,
    Activate,
    Stop,
    Run,
    Submit,
    Stream,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum CliOptionKind {
    Value,
    Flag,
    Repeatable,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct CliOptionSpec {
    pub name: &'static str,
    pub kind: CliOptionKind,
    pub required: bool,
    pub project_path: bool,
    pub allowed_values: &'static [&'static str],
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
