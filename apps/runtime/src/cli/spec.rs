#[allow(clippy::too_many_arguments)]
const fn spec(
    command: &'static str,
    path: &'static [&'static str],
    policy: CliCommandPolicy,
    scope: &'static str,
    risk: &'static str,
    requires: &'static str,
    writes: &'static str,
    input: &'static str,
    output: &'static str,
    minimum_positionals: usize,
    maximum_positionals: usize,
    project_positional: Option<usize>,
    options: &'static [CliOptionSpec],
    errors: &'static [&'static str],
) -> CliCommandSpec {
    CliCommandSpec {
        command,
        path,
        policy,
        scope,
        risk,
        requires,
        writes,
        input,
        output,
        minimum_positionals,
        maximum_positionals,
        project_positional,
        options,
        errors,
    }
}

#[must_use]
pub const fn command_specs() -> &'static [CliCommandSpec] {
    SPECS
}

#[must_use]
pub fn command_spec(path: &[String]) -> Option<&'static CliCommandSpec> {
    SPECS.iter().find(|spec| {
        spec.path.len() == path.len()
            && spec
                .path
                .iter()
                .zip(path)
                .all(|(expected, actual)| *expected == actual)
    })
}

#[must_use]
pub fn command_errors(command: &str) -> String {
    let mut errors = vec![
        "invalid_command",
        "invalid_argument",
        "missing_argument",
        "invalid_input",
        "internal_error",
    ];
    if let Some(spec) = SPECS.iter().find(|spec| spec.command == command) {
        for error in policy_errors(spec.policy).iter().chain(spec.errors) {
            if !errors.contains(error) {
                errors.push(error);
            }
        }
    }
    errors.join(",")
}

const fn policy_errors(policy: CliCommandPolicy) -> &'static [&'static str] {
    match policy {
        CliCommandPolicy::Local => &[],
        CliCommandPolicy::Observe => OBSERVE_ERRORS,
        CliCommandPolicy::Activate => ACTIVATE_ERRORS,
        CliCommandPolicy::Stop => STOP_ERRORS,
        CliCommandPolicy::Run | CliCommandPolicy::Submit | CliCommandPolicy::Stream => {
            RUNTIME_COMMAND_ERRORS
        }
    }
}
